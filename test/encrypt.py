import cocotb
from spade import SpadeExt
from cocotb.triggers import Timer

@cocotb.test
async def encrypt(dut):
    s = SpadeExt(dut)

    s.i.block = [
        0x01, 0x02, 0x03, 0x04,
        0x05, 0x06, 0x07, 0x08,
        0x09, 0x0a, 0x0b, 0x0c,
        0x0d, 0x0e, 0x0f, 0x10,
    ]

    s.i.key = [
        0x10, 0x0f, 0x0e, 0x0d,
        0x0c, 0x0b, 0x0a, 0x09,
        0x08, 0x07, 0x06, 0x05,
        0x04, 0x03, 0x02, 0x01,
    ]

    await Timer(10, units="ns")

    s.o.assert_eq([
        0xcc, 0xdd, 0x71, 0xd5, 
        0xae, 0xae, 0x5c, 0x62, 
        0x33, 0xc5, 0x9c, 0xab, 
        0xc2, 0x92, 0xe9, 0x41        
    ])
